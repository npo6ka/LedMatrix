static void drawSprite(int x, int y, uint32_t *spr)
{
    for (int i = 0 ; i < W ; ++i)
    {
        for (int j = 0 ; j < H ; ++j)
        {
            int v = pgm_read_dword(spr + i + j * W);
            LedMatrix.at(x + i, y + j) = CRGB(v);
        }
    }
}

void Mouse::on_update()
{
    LedMatrix.clear();
    x = (x + 1) % WIDTH;
    drawSprite(x, 0, x % 2 ? mouse1 : mouse2);
    drawSprite(x - WIDTH, 0, x % 2 ? mouse1 : mouse2);
}
